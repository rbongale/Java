package operatordemo;

public class Decrement_Operator {

	public static void main(String[] args) {
		int i=4;
		i--; // i = i-1
		System.out.println(i);
		i--; // i = i-1
		System.out.println(i);
		i--; // i = i-1
		System.out.println(i);
		i--; // i = i-1
		System.out.println(i);
		i--; // i = i-1
		System.out.println(i);
		i--; // i = i-1
		System.out.println(i);

	}

}
