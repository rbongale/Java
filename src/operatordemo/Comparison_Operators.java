package operatordemo;

public class Comparison_Operators {

	public static void main(String[] args) {
		int i=10;
		int j=20;
		System.out.println(i==j);
		System.out.println(i!=j);
		System.out.println(j>i);
		System.out.println(j<i);
		System.out.println(j>=i);
		System.out.println(j<=i);
		

	}

}
